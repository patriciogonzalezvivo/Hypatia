Constellation::Constellation( int _id ) : m_id(-1) {
    setId( _id );
};

Constellation::Constellation( double _ra, double _dec ) : m_id(-1) {
    setId( getConstellationId(_ra, _dec) );
}

Constellation::Constellation( const EqPoint &_point ) : m_id(-1) {
    double ra = _point.getRightAscension();
    double dec = _point.getDeclination();
    setId( getConstellationId( ra, dec ) );
}

Constellation::Constellation( char * _abbr ) : m_id(-1) {
    for (int i = 0; i < N_CONSTELATIONS; i++) {
        if (strncmp (_abbr, cns_namemap[i], 3) == 0) {
            setId( i );
            break;
        }
    }
}

void Constellation::setId( int _id ) {
    if ( _id < 0 || _id >= N_CONSTELATIONS ) {
        return;
    }

    m_id = _id;

    m_sIndices.clear();
    for (int i = 1; i <= cns_stars[_id][0]; i++) {
        m_sIndices.push_back(cns_stars[_id][i]);
    }
    
    m_boundary.clear();
    for (int i = 1; i <= cns_boundaries[_id][0]; i+=2) {
        m_boundary.push_back(EqPoint(cns_boundaries[_id][i], cns_boundaries[_id][i+1]));
    }
}

/* given a constellation id (as from cns_pick()), return pointer to static
 * storage containg its name in the form "AAA: Name".
 * return "???: ????" if id is invalid.
 */

char* Constellation::getName() const {
    if ( m_id < 0 ) {
        return ((char*)"????");
    }
    int len = strlen(cns_namemap[m_id]);// / sizeof(cns_namemap[m_id][0]); //strlen(cns_namemap[m_id]);
    char *cstr = new char[len];
    strncpy(cstr, cns_namemap[m_id]+5, len);
    return cstr;
}

char* Constellation::getAbbreviation() const {
    if ( m_id < 0 ) {
        return ((char*)"???");
    }
    char *cstr = new char[3];
    strncpy(cstr, cns_namemap[m_id], 3);
    return cstr;
}
